const ENGLISH_PATTERNS = [
  [/^(\d+) testimoni$/i, '$1 testimonials'],
  [/^(\d+) keuntungan belajar$/i, '$1 learning benefits'],
  [/^(\d+) terjual$/i, '$1 sold'],
  [/^Membalas (.+)$/i, 'Replying to $1'],
  [/^Catatan admin: (.+)$/i, 'Admin note: $1'],
  [/^Tiket #(.*)$/i, 'Ticket #$1'],
  [/^Menu (Member|Admin)$/i, '$1 menu'],
]

export function translateUiText(value, language) {
  if (language !== 'en' || typeof value !== 'string' || !value.trim()) {
    return value
  }

  const leading = value.match(/^\s*/)?.[0] || ''
  const trailing = value.match(/\s*$/)?.[0] || ''
  const source = value.trim()
  const exact = ENGLISH_UI[source]

  if (exact) {
    return `${leading}${exact}${trailing}`
  }

  for (const [pattern, replacement] of ENGLISH_PATTERNS) {
    if (pattern.test(source)) {
      return `${leading}${source.replace(pattern, replacement)}${trailing}`
    }
  }

  return value
}
